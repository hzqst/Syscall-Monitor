std::wstring UTF8ToUnicode(std::string &str)
{
	std::wstring result;
    int len = MultiByteToWideChar(CP_UTF8, 0, str.c_str(), (int)str.length(), NULL, 0);
	result.resize(len);
    MultiByteToWideChar(CP_UTF8, 0, str.c_str(), (int)str.length(), (LPWSTR)result.data(), len);
	return result;
}

